// Public operations
// ---------------------------------------------------------------------------

/// Sign `data` with raw key material.
///
/// Note: `CKM_RSA_PKCS` is not in the advertised mechanism list (see lib.rs MECHANISMS)
/// because wolfcrypt's sign_pkcs1v15_with_digest double-hashes conformant callers.
/// The match arm below is kept for completeness but is unreachable from lib.rs.
///
/// For `CKM_ECDSA`, `data` is a pre-computed hash (exactly 32 bytes for P-256).
/// For `CKM_ECDSA_SHA256`, `data` is the raw message (hashed internally).
///
/// ECDSA signatures are returned as raw r||s per PKCS#11 s.2.3.1:
/// 32 bytes r followed by 32 bytes s, each zero-padded to the field size (P256_SIG_LEN = 64).
pub fn sign_raw(
    key_type: KeyType,
    der_bytes: &[u8],
    mechanism: CK_MECHANISM_TYPE,
    data: &[u8],
) -> Result<Vec<u8>, OpsError> {
    match mechanism {
        CKM_RSA_PKCS => {
            if key_type != KeyType::Rsa {
                return Err(OpsError::KeyTypeInconsistent);
            }
            let key = RsaPrivateKey::from_pkcs1_der(der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let sig = key
                .sign_pkcs1v15_with_digest(data, RsaDigest::Sha256)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            Ok(sig.as_ref().to_vec())
        }
        CKM_RSA_PKCS_PSS => {
            if key_type != KeyType::Rsa {
                return Err(OpsError::KeyTypeInconsistent);
            }
            let key = RsaPrivateKey::from_pkcs1_der(der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let sig = key
                .sign_pss_with_digest(data, RsaDigest::Sha256)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            Ok(sig.as_ref().to_vec())
        }
        CKM_ECDSA => {
            if key_type != KeyType::Ec {
                return Err(OpsError::KeyTypeInconsistent);
            }
            // `data` is a pre-computed hash. P-256 requires exactly 32 bytes
            // (SHA-256 output). Reject other lengths before calling sign_prehash:
            // wolfcrypt accepts any length silently but produces an invalid
            // (non-verifiable) signature for wrong-length input.
            if data.len() != P256_HASH_LEN {
                return Err(OpsError::DataLenRange);
            }
            let key = P256SigningKey::from_private_key_bytes(der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let sig = key
                .sign_prehash(data)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            // Return raw r||s per PKCS#11 s.2.3.1; wolfcrypt's EcdsaSignature
            // stores r||s internally (not DER) -- as_ref() returns those bytes.
            Ok(sig.as_ref().to_vec())
        }
        CKM_ECDSA_SHA256 => {
            if key_type != KeyType::Ec {
                return Err(OpsError::KeyTypeInconsistent);
            }
            let key = P256SigningKey::from_private_key_bytes(der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let hash = P256::hash_message(data)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let sig = key
                .sign_prehash(&hash)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            // Return raw r||s per PKCS#11 s.2.3.1.
            Ok(sig.as_ref().to_vec())
        }
        _ => Err(OpsError::MechanismInvalid),
    }
}

/// Sign `data` with the key in `key_entry` using the given PKCS#11 mechanism.
pub fn sign(
    key_entry: &KeyEntry,
    mechanism: CK_MECHANISM_TYPE,
    data: &[u8],
) -> Result<Vec<u8>, OpsError> {
    if mechanism == CKM_ML_DSA {
        if key_entry.key_type != KeyType::MlDsa65 {
            return Err(OpsError::KeyTypeInconsistent);
        }
        let pub_bytes = key_entry.pub_bytes.as_deref()
            .ok_or_else(|| OpsError::Crypto("ML-DSA-65 key missing public bytes".to_string()))?;
        let sk = MlDsa65SigningKey::from_key_bytes(&key_entry.der_bytes, pub_bytes)
            .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
        let sig: MlDsa65Signature = sk.try_sign(data)
            .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
        return Ok(sig.as_ref().to_vec());
    }
    sign_raw(key_entry.key_type, &key_entry.der_bytes, mechanism, data)
}

/// Verify `signature` over `data` with the public key derived from `key_entry`.
pub fn verify(
    key_entry: &KeyEntry,
    mechanism: CK_MECHANISM_TYPE,
    data: &[u8],
    signature: &[u8],
) -> Result<(), OpsError> {
    if mechanism == CKM_ML_DSA {
        if key_entry.key_type != KeyType::MlDsa65 {
            return Err(OpsError::KeyTypeInconsistent);
        }
        let pub_bytes = key_entry.pub_bytes.as_deref()
            .ok_or_else(|| OpsError::Crypto("ML-DSA-65 key missing public bytes".to_string()))?;
        let vk = MlDsa65VerifyingKey::from_bytes(pub_bytes)
            .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
        let sig = MlDsa65Signature::try_from(signature)
            .map_err(|_| OpsError::Crypto("invalid ML-DSA-65 signature length".to_string()))?;
        return vk.verify(data, &sig)
            .map_err(|_| OpsError::Crypto("ML-DSA-65 verify failed".to_string()));
    }
    match mechanism {
        CKM_RSA_PKCS => {
            if key_entry.key_type != KeyType::Rsa {
                return Err(OpsError::KeyTypeInconsistent);
            }
            let key = RsaPrivateKey::from_pkcs1_der(&key_entry.der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let pub_key = key.public_key();
            let sig = wolfcrypt::rsa::RsaPkcs1v15Signature::try_from(signature)
                .map_err(|_| OpsError::Crypto("invalid RSA PKCS#1v1.5 signature bytes".to_string()))?;
            pub_key
                .verify_pkcs1v15_with_digest(data, &sig, RsaDigest::Sha256)
                .map_err(|_| OpsError::Crypto("RSA PKCS#1v1.5 verify failed".to_string()))
        }
        CKM_RSA_PKCS_PSS => {
            if key_entry.key_type != KeyType::Rsa {
                return Err(OpsError::KeyTypeInconsistent);
            }
            let key = RsaPrivateKey::from_pkcs1_der(&key_entry.der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let pub_key = key.public_key();
            let sig = wolfcrypt::rsa::RsaPssSignature::try_from(signature)
                .map_err(|_| OpsError::Crypto("invalid RSA-PSS signature bytes".to_string()))?;
            pub_key
                .verify_pss_with_digest(data, &sig, RsaDigest::Sha256)
                .map_err(|_| OpsError::Crypto("RSA-PSS verify failed".to_string()))
        }
        CKM_ECDSA => {
            // PKCS#11 s.2.3.1: `signature` is raw r||s bytes -- P256_SIG_LEN = 64
            // bytes for P-256 (32 bytes r followed by 32 bytes s, zero-padded).
            // wolfcrypt's verify_prehash expects DER; convert here.
            if key_entry.key_type != KeyType::Ec {
                return Err(OpsError::KeyTypeInconsistent);
            }
            // Reject wrong-length prehash for the same reason as in sign().
            if data.len() != P256_HASH_LEN {
                return Err(OpsError::DataLenRange);
            }
            if signature.len() != P256_SIG_LEN {
                return Err(OpsError::Crypto(format!(
                    "CKM_ECDSA signature must be {P256_SIG_LEN} bytes (raw r||s), got {}",
                    signature.len()
                )));
            }
            let sig_der = rs_to_der(&signature[..P256_FIELD_LEN], &signature[P256_FIELD_LEN..])?;
            let key = P256SigningKey::from_private_key_bytes(&key_entry.der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let pub_key = key.verifying_key()
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            pub_key
                .verify_prehash(data, &sig_der)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))
        }
        CKM_ECDSA_SHA256 => {
            // PKCS#11 s.2.3.1: `signature` is raw r||s bytes (same as CKM_ECDSA).
            // data is the raw message; hash internally then verify against DER sig.
            if key_entry.key_type != KeyType::Ec {
                return Err(OpsError::KeyTypeInconsistent);
            }
            if signature.len() != P256_SIG_LEN {
                return Err(OpsError::Crypto(format!(
                    "CKM_ECDSA_SHA256 signature must be {P256_SIG_LEN} bytes (raw r||s), got {}",
                    signature.len()
                )));
            }
            let sig_der = rs_to_der(&signature[..P256_FIELD_LEN], &signature[P256_FIELD_LEN..])?;
            let hash = P256::hash_message(data)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let key = P256SigningKey::from_private_key_bytes(&key_entry.der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let pub_key = key.verifying_key()
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            pub_key
                .verify_prehash(&hash, &sig_der)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))
        }
        _ => Err(OpsError::MechanismInvalid),
    }
}

/// Encrypt `plaintext` using the public key in `key_entry`.
///
/// For `CKM_ML_KEM`, `plaintext` is unused -- encapsulation generates a fresh
/// random shared secret. The output is `ciphertext || shared_secret` (ML-KEM
/// ciphertext size + 32 bytes). The caller sends the ciphertext portion to the
/// decapsulating party; both parties derive the same shared secret.
pub fn encrypt(
    key_entry: &KeyEntry,
    mechanism: CK_MECHANISM_TYPE,
    plaintext: &[u8],
) -> Result<Vec<u8>, OpsError> {
    match mechanism {
        CKM_RSA_PKCS_OAEP => {
            if key_entry.key_type != KeyType::Rsa {
                return Err(OpsError::KeyTypeInconsistent);
            }
            let key = RsaPrivateKey::from_pkcs1_der(&key_entry.der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let pub_key = key.public_key();
            pub_key
                .encrypt_oaep(plaintext)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))
        }
        CKM_ML_KEM => {
            let _ = plaintext;
            if key_entry.key_type != KeyType::MlKem768 {
                return Err(OpsError::KeyTypeInconsistent);
            }
            let pub_bytes = key_entry.pub_bytes.as_deref()
                .ok_or_else(|| OpsError::Crypto("ML-KEM-768 key missing public bytes".to_string()))?;
            let mut ek = MlKem768EncapsulationKey::from_bytes(pub_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let (ct, ss) = ek.encapsulate()
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let mut out = ct;
            out.extend_from_slice(ss.as_bytes());
            Ok(out)
        }
        _ => Err(OpsError::MechanismInvalid),
    }
}

/// Decrypt `ciphertext` using the private key in `key_entry`.
///
/// For `CKM_ML_KEM`, `ciphertext` must be exactly the ML-KEM-768 ciphertext
/// (1088 bytes). Returns the 32-byte shared secret.
pub fn decrypt(
    key_entry: &KeyEntry,
    mechanism: CK_MECHANISM_TYPE,
    ciphertext: &[u8],
) -> Result<Vec<u8>, OpsError> {
    match mechanism {
        CKM_RSA_PKCS_OAEP => {
            if key_entry.key_type != KeyType::Rsa {
                return Err(OpsError::KeyTypeInconsistent);
            }
            let key = RsaPrivateKey::from_pkcs1_der(&key_entry.der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            key.decrypt_oaep(ciphertext)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))
        }
        CKM_ML_KEM => {
            if key_entry.key_type != KeyType::MlKem768 {
                return Err(OpsError::KeyTypeInconsistent);
            }
            // FIPS 203 s.6.3: ML-KEM-768 ciphertext is exactly 1088 bytes.
            // Reject before calling native code; some wolfcrypt builds do not
            // validate length internally and may read past the buffer end.
            const ML_KEM_768_CIPHERTEXT_LEN: usize = 1088;
            if ciphertext.len() != ML_KEM_768_CIPHERTEXT_LEN {
                return Err(OpsError::DataLenRange);
            }
            let dk = MlKem768DecapsulationKey::from_private_bytes(&key_entry.der_bytes)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            let ss = dk.decapsulate(ciphertext)
                .map_err(|e| OpsError::Crypto(format!("{e:?}")))?;
            Ok(ss.as_bytes().to_vec())
        }
        _ => Err(OpsError::MechanismInvalid),
    }
}

