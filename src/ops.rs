/// Encode an ECDSA (r, s) scalar pair as a DER SEQUENCE { INTEGER r, INTEGER s }.
///
/// Uses multi-byte DER length encoding (X.690 s.8.1.3) for any payload size.
/// Short-form (1 byte) is valid only for lengths 0-127; larger values require a
/// long-form prefix (0x81 for 128-255, 0x82 for 256-65535). P-256 and P-384
/// always produce short-form SEQUENCE payloads; P-521's payload is 138 bytes and
/// requires long-form -- a naive single-byte push would silently produce invalid
/// DER that signature parsers reject without a clear error.
///
/// Returns `Err(OpsError::Crypto)` if either component is empty. Wolfcrypt always
/// returns fixed-size non-empty scalars (P-256: 32 bytes each), so this path is
/// unreachable in practice -- but returning an error is safe across the PKCS#11
/// FFI boundary, whereas a panic is undefined behaviour.
pub fn rs_to_der(r: &[u8], s: &[u8]) -> Result<Vec<u8>, OpsError> {
    /// X.690 s.8.1.3 definite-length encoding.
    fn der_len(n: usize) -> Vec<u8> {
        if n < 0x80 {
            vec![n as u8]
        } else if n < 0x100 {
            vec![0x81, n as u8]
        } else {
            // Handles up to 65535 bytes -- sufficient for any practical elliptic curve.
            vec![0x82, (n >> 8) as u8, (n & 0xFF) as u8]
        }
    }

    /// Encode a single DER INTEGER from an unsigned big-endian byte slice.
    ///
    /// Returns `None` if `n` is empty. Wolfcrypt never returns empty components,
    /// but we cannot panic across FFI so we return an error instead.
    fn encode_int(n: &[u8]) -> Option<Vec<u8>> {
        if n.is_empty() {
            // Empty scalar is never produced by wolfcrypt, but would cause
            // usize::MAX underflow in the unwrap_or path -- return None so
            // the caller can propagate an OpsError rather than panic.
            return None;
        }
        // Strip leading zero bytes but keep at least one byte (index n.len()-1
        // is safe here because we checked n.is_empty() above).
        let trimmed: &[u8] = &n[n.iter().position(|&b| b != 0).unwrap_or(n.len() - 1)..];
        // Prepend 0x00 if the high bit is set (DER requires non-negative integers).
        let needs_pad = trimmed[0] & 0x80 != 0;
        let int_len = trimmed.len() + usize::from(needs_pad);
        let mut out = vec![0x02]; // INTEGER tag
        out.extend(der_len(int_len));
        if needs_pad {
            out.push(0x00);
        }
        out.extend_from_slice(trimmed);
        Some(out)
    }

    let r_enc = encode_int(r)
        .ok_or_else(|| OpsError::Crypto("ECDSA r component is empty".to_string()))?;
    let s_enc = encode_int(s)
        .ok_or_else(|| OpsError::Crypto("ECDSA s component is empty".to_string()))?;
    let payload = r_enc.len() + s_enc.len();
    let mut der = vec![0x30]; // SEQUENCE tag
    der.extend(der_len(payload));
    der.extend(r_enc);
    der.extend(s_enc);
    Ok(der)
}
