/// PKCS#11 cryptographic operations against key material loaded into the token.
///
/// Each function borrows a [`KeyEntry`] in-place; key bytes are never copied
/// outside the mlock'd allocation held by the Keystore.
use cryptoki_sys::{
    CK_MECHANISM_TYPE, CKM_ECDSA, CKM_ECDSA_SHA256, CKM_ML_DSA, CKM_ML_KEM,
    CKM_RSA_PKCS, CKM_RSA_PKCS_OAEP, CKM_RSA_PKCS_PSS,
};
use signature::{Signer as _, Verifier as _};
use wolfcrypt::{
    MlDsa65Signature, MlDsa65SigningKey, MlDsa65VerifyingKey,
    MlKem768DecapsulationKey, MlKem768EncapsulationKey,
    rsa::{RsaDigest, RsaPrivateKey},
    EcdsaCurve, P256, P256SigningKey,
};

use crate::keystore::{KeyEntry, KeyType};

/// Required prehash length for P-256 / CKM_ECDSA.
///
/// PKCS#11 s.11.11 (CKM_ECDSA): "The input to the C_Sign function is the
/// hash value generated by the hash algorithm used with the key." For P-256
/// the standard algorithm is SHA-256, producing exactly 32 bytes. Wolfcrypt's
/// `sign_prehash` accepts any length without erroring, but passing a wrong-
/// length hash produces an invalid (non-verifiable) signature -- we reject it
/// early to surface the caller error as CKR_DATA_LEN_RANGE.
const P256_HASH_LEN: usize = 32;

/// Byte length of a P-256 field element (group order = 256 bits = 32 bytes).
///
/// Used to split a raw r||s ECDSA signature into its two components.
/// Coincidentally equal to `P256_HASH_LEN` (SHA-256 also produces 32 bytes),
/// but the two constants serve different purposes: `P256_HASH_LEN` guards
/// the pre-hash input length; `P256_FIELD_LEN` indexes into the signature
/// byte array. (soft_PKCS11-5l26)
const P256_FIELD_LEN: usize = 32;

/// Fixed raw r||s signature length for P-256 per PKCS#11 s.2.3.1.
///
/// PKCS#11 s.2.3.1: "For the ECDSA mechanism ... the signature is the
/// concatenation of r and s, each of the same length as the base prime." For
/// P-256 (256-bit prime) each component is exactly 32 bytes -> 64 bytes total.
const P256_SIG_LEN: usize = 64;

// ---------------------------------------------------------------------------
// Error type
// ---------------------------------------------------------------------------

#[derive(Debug)]
pub enum OpsError {
    MechanismInvalid,
    KeyTypeInconsistent,
    /// The data length is wrong for the mechanism.
    ///
    /// `CKM_ECDSA` requires exactly `P256_HASH_LEN` (32) bytes -- the caller is
    /// responsible for hashing before calling `C_Sign`. Passing a SHA-1 (20-byte)
    /// or SHA-384 (48-byte) digest produces undefined wolfcrypt behavior; we reject
    /// it here and propagate as `CKR_DATA_LEN_RANGE` per PKCS#11 s.11.11.
    DataLenRange,
    Crypto(String),
}

