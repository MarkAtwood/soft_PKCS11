// ---------------------------------------------------------------------------
// rs_to_der: multi-byte DER length encoding
// ---------------------------------------------------------------------------
//
// DER X.690 s.8.1.3: lengths >= 128 must use long-form encoding (0x81 prefix
// for 128-255 bytes). Passing 64-byte r/s (high bit set, requiring 0x00 pad)
// produces a SEQUENCE payload of 134 bytes -- which requires 0x81 0x86 in the
// length field. A 1-byte push of 0x86 would be parsed as INTEGER length by
// most DER parsers, producing invalid output.

#[test]
fn rs_to_der_long_form_sequence_length() {
    // 64-byte scalars: the 0x80 leading byte forces a 0x00 pad in each INTEGER.
    // Each encode_int output: [0x02, 0x41, 0x00, 0x80, 0x00 x 63] = 67 bytes.
    // Payload = 67 + 67 = 134 = 0x86. Requires long-form SEQUENCE length.
    let r = {
        let mut v = vec![0u8; 64];
        v[0] = 0x80;
        v
    };
    let s = r.clone();

    let der = ops::rs_to_der(&r, &s).expect("rs_to_der with non-empty scalars must not fail");

    // Verify SEQUENCE tag and long-form length prefix.
    assert_eq!(der[0], 0x30, "DER must start with SEQUENCE tag 0x30");
    assert_eq!(der[1], 0x81, "payload 134 requires long-form length 0x81 prefix");
    assert_eq!(der[2], 134,  "long-form length byte must be 134 (0x86)");

    // Each INTEGER: tag 0x02, length 0x41 (65), 0x00 pad, then 64 bytes.
    assert_eq!(der[3],  0x02, "first INTEGER tag");
    assert_eq!(der[4],  0x41, "first INTEGER length (65 bytes)");
    assert_eq!(der[5],  0x00, "first INTEGER must have 0x00 pad (high bit set)");
    assert_eq!(der[6],  0x80, "first INTEGER first data byte");

    let second_int_offset = 3 + 2 + 65; // tag + len + 65 content bytes
    assert_eq!(der[second_int_offset], 0x02, "second INTEGER tag");
    assert_eq!(der.len(), 3 + 134,  "total DER length must be tag(1) + long-len(2) + payload(134)");
}

#[test]
fn rs_to_der_short_form_p256() {
    // P-256 scalars: 32 bytes each. Maximum output per INTEGER = 35 bytes.
    // Maximum payload = 70 bytes < 128, so short-form length is correct.
    let r = {
        let mut v = vec![0u8; 32];
        v[0] = 0x80; // force 0x00 pad: int_len = 33, still short-form
        v
    };
    let s = vec![0x01u8; 32]; // no high bit: int_len = 32, no pad needed

    let der = ops::rs_to_der(&r, &s).expect("rs_to_der with non-empty scalars must not fail");

    assert_eq!(der[0], 0x30, "SEQUENCE tag");
    // payload < 128 -> short-form: second byte is the length directly
    assert!(der[1] < 0x80, "P-256 payload must use short-form length (< 0x80)");
}
