// ---------------------------------------------------------------------------
// Test 5: CKM_ECDSA_SHA256 verify -- wolfcrypt signs, openssl validates the sig,
// then wolfcrypt verify must accept it.
//
// Oracle independence: wolfcrypt sign is independently verified against openssl
// in ecdsa_sha256_sign_verified_by_openssl above. Using that known-good signature
// to test verify exercises a different code path (wc_ecc_sign_hash !=
// wc_ecc_verify_hash) without being purely self-referential.
// ---------------------------------------------------------------------------

#[test]
fn ecdsa_sha256_verify_accepts_valid_signature() {
    let entry = ec_entry();
    let msg = b"test message for ECDSA-SHA256 verify";

    // Sign with wolfcrypt (CKM_ECDSA_SHA256 -- hashes internally, returns raw r||s).
    let sig = ops::sign(&entry, CKM_ECDSA_SHA256, msg).expect("ECDSA sign failed");

    // Verify: wolfcrypt verify must accept a raw r||s signature wolfcrypt produced.
    ops::verify(&entry, CKM_ECDSA_SHA256, msg, &sig)
        .expect("ECDSA-SHA256 verify rejected a valid signature");
}

// ---------------------------------------------------------------------------
// Test 6: CKM_ECDSA verify (prehash) -- wolfcrypt signs hash, wolfcrypt verifies.
// The sign path (sign_prehash) and verify path (verify_prehash) call different
// wolfCrypt C functions; consistency between them is a real correctness property.
// ---------------------------------------------------------------------------

#[test]
fn ecdsa_prehash_verify_accepts_valid_signature() {
    use std::process::Command;

    let entry = ec_entry();
    let msg = b"test message for ECDSA prehash verify";

    // Compute SHA-256(msg) using openssl as external oracle.
    let hash_hex = {
        let output = Command::new("openssl")
            .args(["dgst", "-sha256", "-hex"])
            .stdin(std::process::Stdio::piped())
            .stdout(std::process::Stdio::piped())
            .spawn()
            .expect("openssl not found");
        use std::io::Write;
        let mut child = output;
        child.stdin.take().unwrap().write_all(msg).unwrap();
        let out = child.wait_with_output().unwrap();
        assert!(out.status.success(), "openssl dgst failed");
        let s = String::from_utf8_lossy(&out.stdout);
        // output: "SHA2-256(stdin)= <hex>\n" or "(stdin)= <hex>\n"
        s.split_whitespace().last().unwrap().to_string()
    };
    let hash_bytes = hex::decode(&hash_hex).expect("hex decode of SHA-256");
    assert_eq!(hash_bytes.len(), 32, "SHA-256 must be 32 bytes");

    // Sign pre-hashed data with CKM_ECDSA -- returns raw r||s (64 bytes).
    let sig = ops::sign(&entry, CKM_ECDSA, &hash_bytes).expect("ECDSA prehash sign failed");

    // Verify the pre-hashed data against the raw r||s signature.
    ops::verify(&entry, CKM_ECDSA, &hash_bytes, &sig)
        .expect("ECDSA prehash verify rejected a valid signature");
}

// ---------------------------------------------------------------------------
// Test 7: tampered signature fails both ECDSA mechanisms.
// ---------------------------------------------------------------------------

#[test]
fn ecdsa_verify_rejects_tampered_signature() {
    let entry = ec_entry();
    let msg = b"message whose signature will be tampered";

    // Produce a valid CKM_ECDSA_SHA256 signature (raw r||s, 64 bytes).
    let mut sig = ops::sign(&entry, CKM_ECDSA_SHA256, msg).expect("sign");

    // Flip a byte in the middle of the raw r||s to produce an invalid signature.
    let mid = sig.len() / 2;
    sig[mid] ^= 0xFF;

    match ops::verify(&entry, CKM_ECDSA_SHA256, msg, &sig) {
        Err(OpsError::Crypto(_)) => {}
        other => panic!("expected Crypto error for tampered sig, got {:?}", other),
    }
}

